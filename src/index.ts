/**
 * 加
 * @param a 
 * @param b 
 */
export function add(a: number, b: number): number {
    return a + b;
}

/**
 * 减
 * @param a 
 * @param b 
 */

export function sub(a: number, b: number): number {
    return a - b
}
/**
 * 除以
 * @param a 
 * @param b 
 */
export function divide(a: number, b: number): number {
    return a / b;
}

/**
 * 乘以
 * @param a 
 * @param b 
 */
export function multiply(a: number, b: number): number {
    return a * b;
}